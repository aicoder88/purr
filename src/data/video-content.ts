// Helper function to get video content by solution type
export function getVideoContent(solutionType: string) {
  return {
    howTo: videoContent.howToVideos[solutionType as keyof typeof videoContent.howToVideos],
    beforeAfter: Object.values(videoContent.beforeAfterVideos).find(video =>
      video.title.toLowerCase().includes(solutionType.replace('-', ' '))
    ),
    testimonials: videoContent.testimonialVideos.filter(video =>
      video.category === solutionType.replace('-odor-control', '').replace('-solution', '').replace('-solutions', '')
    ),
    science: videoContent.scienceVideos['activated-carbon-science']
  };
}

